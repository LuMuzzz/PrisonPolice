package com.muzzz.basic.bean;

public final class DialogBean {

    private boolean mIsShow;
    private String mMsg;

    public boolean isShow() {
        return mIsShow;
    }

    public void setShow(boolean show) {
        mIsShow = show;
    }

    public String getMsg() {
        return mMsg;
    }

    public void setMsg(String msg) {
        this.mMsg = msg;
    }
}
